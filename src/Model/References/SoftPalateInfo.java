package Model.References;

public class SoftPalateInfo implements IRef {

    public String getName() {
        return "soft palate";
    }

    public String getDesc() {
        return "The soft tissue that makes up the back portion of the roof of the mouth. It is distinguished " +
                "from the hard palate in that it doesn't have a hard bone like the hard palate does.";
    }

}

